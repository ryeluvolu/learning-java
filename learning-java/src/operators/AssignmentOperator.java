package operators;

public class AssignmentOperator {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		a+=4; // a = a+4;
		b-=4; // b= b-4;
		a*=4; // a= a * 4 = 10*2
		b/=2; // b = b / 2 =  20/2
		System.out.println(a);
		System.out.println(b);
		System.out.println(a);
		System.out.println(b);

	}

}
