package abstraction;

/**
 * This is an interface example
 * We use keyword "interface" for creating interfaces
 * All methods in interface are abstract methods in interface 
 * We can not instantiate interfaces 
 * We achieve abstraction in Java using abstract classes and interfaces 
 * @author ryelu
 *
 */
public interface Animal {
	
	public void roam();
	public void eat();
	public void makeNoise();

}
