package inheritence;
/**
 * This is sub class or child class extends super class or parent class
 * 
 * In this example "Actor" is subclass which extends super class "Person". So Actor inherits
 * all the members of Person class, this is called inheritance in Java.
 * @author ryelu
 *
 */

public class Actor extends Person {
	
	public void act() {
		System.out.println("I can act");
	}

}
